#!/usr/bin/env python3
"""
This module contains a function that adds
two floats and returns the result as a float.
"""


def add(a: float, b: float) -> float:
    """
    Adds two floats and returns the result.

    Args:
        a (float): The first float to add.
        b (float): The second float to add.

    Returns:
        float: The sum of the two floats.
    """
    return a + b
