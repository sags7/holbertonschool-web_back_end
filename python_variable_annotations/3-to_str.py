#!/usr/bin/env python3
"""
This module contains a function that takes a float
and returns the string representation of it.
"""


def to_str(x: float) -> str:
    """
    Returns the string representation of a float.

    Args:
        x (float): The float to convert to string.

    Returns:
        str: The string representation of the float.
    """
    return str(x)
